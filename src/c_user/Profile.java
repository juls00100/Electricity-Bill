    private void updateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updateMouseClicked
    String fn = fname.getText();
    String ln = lname.getText();
    String em = email.getText();
    
    if(fn.isEmpty() || ln.isEmpty() || em.isEmpty()){
        JOptionPane.showMessageDialog(this, "Palihog ayaw biyai nga walay sulod ang importante nga fields!");
        return;
    }

    try {
        config conf = new config();
        Connection conn = conf.connectDB();
        String sql = "UPDATE users SET u_fname = ?, u_lname = ?, u_email = ? WHERE u_id = ?";
        PreparedStatement pst = conn.prepareStatement(sql);
        
        pst.setString(1, fn);
        pst.setString(2, ln);
        pst.setString(3, em);
        pst.setInt(4, session.getInstance().getId());
        int sentinel = pst.executeUpdate();

        if(sentinel > 0) {
            session sess = session.getInstance();
            sess.setFname(fn);
            sess.setLname(ln);
            sess.setEmail(em);

            JOptionPane.showMessageDialog(null, "Profile Updated Successfully!");
           
            displayUserData(); 
        }

        pst.close();
        conn.close(); 

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage());
        System.out.println("Database Error: " + e.getMessage());
    }
    }//GEN-LAST:event_updateMouseClicked

