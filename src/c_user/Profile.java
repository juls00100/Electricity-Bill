private void displayUserData() {
    // 1. Get the single instance of the session
    session sess = session.getInstance();
    
    // Inside your displayUserData()
    if (sess.getPath() != null && !sess.getPath().isEmpty()) {
        profile.setIcon(resizeImage(sess.getPath(), null));
    } else {
        // Optional: Set a default "no-profile" icon if path is empty
        profile.setText("No Image");
    }
    
    // 2. Set the text of your components using the session data
    // Make sure these variable names (id, fname, lname, etc.) match your Navigator variables
    username.setText(String.valueOf(sess.getId()));
    username.setEditable(false); // ID should not be edited
    profile.setText(sess.getImage());
    fname.setText(sess.getFname());
    lname.setText(sess.getLname());
    email.setText(sess.getEmail());
    username.setText(sess.getUsername());
    address.setText(sess.getAddress());
    accnum.setText(sess.getAccnum());
    accnum.setEditable(false); 
    email.setText(sess.getEmail()); 
    address.setText(sess.getAddress());
}
