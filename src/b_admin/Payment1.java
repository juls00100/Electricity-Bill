    private void addbillsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addbillsActionPerformed
        javax.swing.JTextField fldBillID = new javax.swing.JTextField();
    javax.swing.JTextField fldAccNum = new javax.swing.JTextField();
    javax.swing.JTextField fldAmount = new javax.swing.JTextField();
    javax.swing.JTextField fldCash   = new javax.swing.JTextField();
    javax.swing.JTextField fldMethod = new javax.swing.JTextField();

    Object[] fields = {
        "Bill ID:",    fldBillID,
        "Account #:",  fldAccNum,
        "Amount:",     fldAmount,
        "Cash:",       fldCash,
        "Method:",     fldMethod
    };

    int result = JOptionPane.showConfirmDialog(
        null, fields, "Add Payment", JOptionPane.OK_CANCEL_OPTION
    );

    if (result == JOptionPane.OK_OPTION) {
        try {
            int    bID     = Integer.parseInt(fldBillID.getText().trim());
            double amount  = Double.parseDouble(fldAmount.getText().trim());
            double cash    = Double.parseDouble(fldCash.getText().trim());
            double change  = cash - amount;
            String method  = fldMethod.getText().trim();
            String accNum  = fldAccNum.getText().trim();
            String date    = new java.text.SimpleDateFormat("yyyy-MM-dd").format(new java.util.Date());

            config conf = new config();
            java.sql.Connection conn = conf.connectDB();
            String sql = "INSERT INTO payments (b_id, p_amount, p_cash, p_change, p_method, p_date, u_accnum) " +
                         "VALUES (?, ?, ?, ?, ?, ?, ?)";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.setInt(1, bID);
            pst.setDouble(2, amount);
            pst.setDouble(3, cash);
            pst.setDouble(4, change);
            pst.setString(5, method);
            pst.setString(6, date);
            pst.setString(7, accNum);
            int rows = pst.executeUpdate();
            pst.close();
            conn.close();

            if (rows > 0) {
                JOptionPane.showMessageDialog(null, "Payment added! Change: " + change);
                displayData();
            } else {
                JOptionPane.showMessageDialog(null, "Failed to add payment.");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Please enter valid numbers for Bill ID, Amount, and Cash.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
            e.printStackTrace();
        }
    }
    }//GEN-LAST:event_addbillsActionPerformed

    private void editbillActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editbillActionPerformed
        int selectedRow = paymenttable.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(null, "Please select a payment to edit.");
        return;
    }

    int    pID    = Integer.parseInt(paymenttable.getValueAt(selectedRow, 0).toString());
    String accNum = paymenttable.getValueAt(selectedRow, 1).toString();
    String bID    = paymenttable.getValueAt(selectedRow, 2).toString();
    String paid   = paymenttable.getValueAt(selectedRow, 3).toString();
    String date   = paymenttable.getValueAt(selectedRow, 4).toString();

    // Pre-filled input fields
    javax.swing.JTextField fldBillID = new javax.swing.JTextField(bID);
    javax.swing.JTextField fldAccNum = new javax.swing.JTextField(accNum);
    javax.swing.JTextField fldAmount = new javax.swing.JTextField(paid);
    javax.swing.JTextField fldCash   = new javax.swing.JTextField();
    javax.swing.JTextField fldDate   = new javax.swing.JTextField(date);

    Object[] fields = {
        "Bill ID:",   fldBillID,
        "Account #:", fldAccNum,
        "Amount:",    fldAmount,
        "Cash:",      fldCash,
        "Date:",      fldDate
    };

    int result = JOptionPane.showConfirmDialog(
        null, fields, "Edit Payment ID: " + pID, JOptionPane.OK_CANCEL_OPTION
    );

    if (result == JOptionPane.OK_OPTION) {
        try {
            double amount = Double.parseDouble(fldAmount.getText().trim());
            double cash   = Double.parseDouble(fldCash.getText().trim());
            double change = cash - amount;
            String newDate = fldDate.getText().trim();
            String newAcc  = fldAccNum.getText().trim();
            int    newBID  = Integer.parseInt(fldBillID.getText().trim());

            config conf = new config();
            java.sql.Connection conn = conf.connectDB();
            String sql = "UPDATE payments SET b_id = ?, p_amount = ?, p_cash = ?, p_change = ?, p_date = ?, u_accnum = ? " +
                         "WHERE p_id = ?";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.setInt(1, newBID);
            pst.setDouble(2, amount);
            pst.setDouble(3, cash);
            pst.setDouble(4, change);
            pst.setString(5, newDate);
            pst.setString(6, newAcc);
            pst.setInt(7, pID);
            int rows = pst.executeUpdate();
            pst.close();
            conn.close();

            if (rows > 0) {
                JOptionPane.showMessageDialog(null, "Payment updated successfully!");
                displayData();
            } else {
                JOptionPane.showMessageDialog(null, "Update failed.");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Please enter valid numbers.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
            e.printStackTrace();
        }
    }
    }//GEN-LAST:event_editbillActionPerformed

    private void editbill1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editbill1ActionPerformed
        int selectedRow = paymenttable.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(null, "Please select a payment to delete.");
        return;
    }

    int pID = Integer.parseInt(paymenttable.getValueAt(selectedRow, 0).toString());

    int confirm = JOptionPane.showConfirmDialog(
        null,
        "Are you sure you want to delete Payment ID: " + pID + "?",
        "Delete Confirmation",
        JOptionPane.YES_NO_OPTION
    );

    if (confirm == JOptionPane.YES_OPTION) {
        try {
            config conf = new config();
            java.sql.Connection conn = conf.connectDB();
            String sql = "DELETE FROM payments WHERE p_id = ?";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.setInt(1, pID);
            int rows = pst.executeUpdate();
            pst.close();
            conn.close();

            if (rows > 0) {
                JOptionPane.showMessageDialog(null, "Payment deleted successfully!");
                displayData();
            } else {
                JOptionPane.showMessageDialog(null, "Delete failed. Payment not found.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
            e.printStackTrace();
        }
    }
    }//GEN-LAST:event_editbill1ActionPerformed

