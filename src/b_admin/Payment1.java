  public void displayData() {
    try {
        java.sql.Connection conn = config.connectDB();
        
        // This query connects all 3 tables to get the Account Number
        String sql = "SELECT p.p_id AS 'Payment ID', " +
                     "u.u_accnum AS 'Account #', " + 
                     "p.b_id AS 'Bill ID', " +
                     "p.p_amount AS 'Paid', " +
                     "p.p_date AS 'Date' " +
                     "FROM payments p " +
                     "JOIN bills b ON p.b_id = b.b_id " +
                     "JOIN users u ON b.u_id = u.u_id";

        java.sql.PreparedStatement pst = conn.prepareStatement(sql);
        java.sql.ResultSet rs = pst.executeQuery();
        
        // Fills your JTable
        paymenttable.setModel(DbUtils.resultSetToTableModel(rs));
        
        rs.close();
        pst.close();
        conn.close();
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(null, "Payment Error: " + e.getMessage());
    }
}

